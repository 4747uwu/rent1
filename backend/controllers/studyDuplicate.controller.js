import mongoose from 'mongoose';
import DicomStudy from '../models/dicomStudyModel.js';

// ─── DUPLICATE STUDY (within same org) ───────────────────────────────────────

export const duplicateStudy = async (req, res) => {
    try {
        const { studyId } = req.params;
        const currentUser = req.user;

        if (!currentUser) {
            return res.status(401).json({ success: false, message: 'Unauthorized' });
        }

        if (!mongoose.Types.ObjectId.isValid(studyId)) {
            return res.status(400).json({ success: false, message: 'Invalid study ID' });
        }

        const sourceStudy = await DicomStudy.findById(studyId).lean();
        if (!sourceStudy) {
            return res.status(404).json({ success: false, message: 'Study not found' });
        }

        // Org scope check
        if (
            currentUser.role !== 'super_admin' &&
            sourceStudy.organizationIdentifier !== currentUser.organizationIdentifier
        ) {
            return res.status(403).json({ success: false, message: 'Access denied' });
        }

        // Build the duplicate — strip fields that must be unique/reset
        const {
            _id,
            bharatPacsId: _oldBharatPacsId,
            createdAt,
            updatedAt,
            __v,
            copiedFrom,
            copiedTo,
            duplicatedFrom: _df,
            duplicatedTo: _dt,
            isDuplicateStudy: _isDup,
            isCopiedStudy,
            actionLog,
            statusHistory,
            reports,
            modernReports,
            uploadedReports,
            doctorReports,
            reportInfo,
            verificationInfo,
            revertInfo,
            printHistory,
            preProcessedDownload,
            lastDownload,
            discussions,
            isLocked,
            lockedBy,
            lockedByName,
            lockedByRole,
            lockedAt,
            lockReason,
            lockExpiry,
            previousLocks,
            billing,
            searchText,
            calculatedTAT,
            timingInfo,
            ...studyFields
        } = sourceStudy;

        const duplicateData = {
            ...studyFields,
            // Reset workflow
            workflowStatus: 'new_study_received',
            currentCategory: 'CREATED',
            ReportAvailable: false,
            assignments: [],
            // Mark as duplicate
            isDuplicateStudy: true,
            duplicatedFrom: {
                studyId: sourceStudy._id,
                bharatPacsId: sourceStudy.bharatPacsId,
                duplicatedAt: new Date(),
                duplicatedBy: currentUser._id,
                duplicatedByName: currentUser.fullName,
            },
            duplicatedTo: [],
            // Reset reports
            reports: [],
            modernReports: [],
            uploadedReports: [],
            doctorReports: [],
            // Reset locks
            isLocked: false,
            // Fresh action log
            actionLog: [{
                actionType: 'study_copied',
                actionCategory: 'workflow',
                performedBy: currentUser._id,
                performedByName: currentUser.fullName,
                performedByRole: currentUser.role,
                performedAt: new Date(),
                details: `Duplicated from ${sourceStudy.bharatPacsId}`,
            }],
        };

        // bharatPacsId will be auto-generated by pre-save middleware
        const duplicatedStudy = await DicomStudy.create(duplicateData);

        // Update source study — mark as duplicated
        await DicomStudy.findByIdAndUpdate(sourceStudy._id, {
            $push: {
                duplicatedTo: {
                    studyId: duplicatedStudy._id,
                    bharatPacsId: duplicatedStudy.bharatPacsId,
                    duplicatedAt: new Date(),
                    duplicatedBy: currentUser._id,
                    duplicatedByName: currentUser.fullName,
                },
                actionLog: {
                    $each: [{
                        actionType: 'study_copied',
                        actionCategory: 'workflow',
                        performedBy: currentUser._id,
                        performedByName: currentUser.fullName,
                        performedByRole: currentUser.role,
                        performedAt: new Date(),
                        details: `Study duplicated as ${duplicatedStudy.bharatPacsId}`,
                    }],
                    $position: 0,
                },
            },
        });

        console.log(`📋 [Duplicate] Study ${sourceStudy.bharatPacsId} → ${duplicatedStudy.bharatPacsId} by ${currentUser.fullName}`);

        return res.status(201).json({
            success: true,
            message: 'Study duplicated successfully',
            data: {
                originalBharatPacsId: sourceStudy.bharatPacsId,
                duplicatedStudyId: duplicatedStudy._id,
                duplicatedBharatPacsId: duplicatedStudy.bharatPacsId,
                studyInstanceUID: duplicatedStudy.studyInstanceUID,
                patientName: duplicatedStudy.patientInfo?.patientName || 'Unknown',
                modality: duplicatedStudy.modality,
            },
        });
    } catch (error) {
        console.error('❌ [Duplicate] Error:', error);
        res.status(500).json({ success: false, message: 'Failed to duplicate study' });
    }
};
